let lastContext = null;
let lastResponseIndex = -1;

// Utility function to save data to local storage
function saveToLocalStorage(type, input, result) {
    let storageKey = type === 'voice' ? 'voiceHistory' : 'searchHistory';
    let storedData = JSON.parse(localStorage.getItem(storageKey)) || [];
    storedData.push({ input: input, result: result, timestamp: new Date().toLocaleString() });
    localStorage.setItem(storageKey, JSON.stringify(storedData));
}

function getResponseCategory(transcript) {
    if (transcript.includes("hello") || transcript.includes("hi") || transcript.includes("hey")) {
        lastContext = 'greetings';
        return greetings;
    } else if (transcript.includes("sick")) {
        lastContext = 'sick';
        return sick;
    } else if (transcript.includes("how are you")) {
        lastContext = 'asking';
        return asking;
    } else if (transcript.includes("bye") || transcript.includes("goodbye") || transcript.includes("see you")) {
        lastContext = 'farewells';
        return farewells;
    } else if (transcript.includes("thank you") || transcript.includes("thanks")) {
        lastContext = 'gratitude';
        return gratitude;
    } else if (transcript.includes("tell me a joke") || transcript.includes("joke")) {
        lastContext = 'jokes';
        return jokes;
    } else if (transcript.includes("bored") || transcript.includes("nothing to do") || transcript.includes("no, suggest me something else") || transcript.includes("do you have any other suggestions")) {
        lastContext = 'boredResponses';
        return boredResponses;
    } else if (transcript.includes("talk to you") || transcript.includes("can we talk")) {
        lastContext = 'friendlyChat';
        return friendlyChat;
    } else if (transcript.includes("shayari")) {
        lastContext = 'shayaris';
        return shayaris;
    } else if (transcript.includes("about yourself")) {
        lastContext = 'yourself';
        return yourself;
    } else if (transcript.includes("write a code") || transcript.includes("generate a code")) {
        lastContext = 'code';
        return code;
    } else if(transcript.includes("have no friends")||transcript.includes("i do not have friends")||transcript.includes("i dont have friends")||transcript.includes("i don't have friends")){
        lastContext='nofriends';
        return nofriends;
    } else {
        lastContext = 'smallTalk';
        return smallTalk;
    }
}

function getDynamicResponse(category) {
    const responses = getResponseCategory(category);
    let randomIndex;

    do {
        randomIndex = Math.floor(Math.random() * responses.length);
    } while (randomIndex === lastResponseIndex);

    lastResponseIndex = randomIndex;
    return responses[randomIndex];
}

// Common function to handle recognized text
function handleTranscript(transcript) {
    recognizedTextDiv.innerHTML += `<p>${transcript}</p>`;
    let result = "";
    if (transcript.includes("what is the time") || transcript.includes("tell me the time") || transcript.includes("current time")) {
        const currentTime = new Date().toLocaleTimeString();
        result = `The current time is ${currentTime}`;
        readOut(result);
    } else if (transcript.includes("near me") || transcript.includes("nearby") || transcript.includes("find a place") || transcript.includes("what's around") || transcript.includes("nearby")) {
        // Extract the type of place user is looking for
        const placeType = transcript.replace(/near me|nearby|find a place|what's around/i, '').trim();
        result = `Searching for you...`;
        readOut(result);
        // Redirect to Google Maps with the search query
        const mapsUrl = `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(placeType)}`;
        window.open(mapsUrl, "_blank");
    } else if (transcript.includes("what is the date") || transcript.includes("tell me the date") || transcript.includes("current date")) {
        const currentDate = new Date().toLocaleDateString();
        result = `Today's date is ${currentDate}`;
        readOut(result);
    } else if (transcript.includes("search") || transcript.includes("top") || transcript.includes("suggest") || transcript.includes("shadow search") || transcript.includes("find") || transcript.includes("who") || transcript.includes("why") || transcript.includes("what") || transcript.includes("when") || transcript.includes("whose") || transcript.includes("which") || transcript.includes("where")) {
        const searchQuery = transcript.replace("shadow", "").trim();
        result = `Here are your results`;
        saveToLocalStorage('voice', transcript, result);
        readOut(result);
        // Opening the custom search engine with the search query
        window.open(`https://sengine.netlify.app/?q=${encodeURIComponent(searchQuery)}`, "_blank");
    } else if (transcript.includes("play")) {
        const searchQuery = transcript.replace("shadow play", "").trim();
        result = `Playing ${searchQuery}`;
        saveToLocalStorage('voice', transcript, result);
        readOut(result);
        window.open(`https://music.youtube.com/search?q=${encodeURIComponent(searchQuery)}&src=yt_music&feature=share`, "_blank");
    } else if (transcript.includes("tell me something else")) {
        if (lastContext) {
            result = getDynamicResponse(lastContext);
            readOut(result);
        } else {
            result = "I'm not sure what you want to hear about. Can you clarify?";
            readOut(result);
        }
    } else {
        let found = false;
        for (const [keyword, url] of Object.entries(urlMapping)) {
            if (transcript.includes(`open ${keyword}`)) {
                result = `Opening ${keyword}`;
                readOut(result);
                setTimeout(() => {
                    window.open(url, "_blank");
                }, 500);
                found = true;
                break;
            }
        }

        if (!found) {
            let response = getDynamicResponse(transcript);
            result = response;
            readOut(response);
        }
    }

    // Save the voice input and result
    saveToLocalStorage('voice', transcript, result);
}

