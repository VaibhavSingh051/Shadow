// Function to start the camera feed
async function startCamera() {
    try {
        const stream = await navigator.mediaDevices.getUserMedia({ video: true });
        const videoElement = document.getElementById('camera-feed');
        videoElement.srcObject = stream;
    } catch (error) {
        console.error('Error accessing the camera:', error);
    }
}

// Function to fetch emotion analysis from the Flask server
async function getEmotionAnalysis() {
    try {
        // Create a canvas element to capture the current frame from the video
        const canvas = document.createElement('canvas');
        const video = document.getElementById('camera-feed');
        canvas.width = video.videoWidth;
        canvas.height = video.videoHeight;
        const context = canvas.getContext('2d');
        context.drawImage(video, 0, 0, canvas.width, canvas.height);

        // Convert the canvas image to Base64 encoded JPEG
        const imageData = canvas.toDataURL('image/jpeg');
        const base64Image = imageData.split(',')[1]; // Extract the Base64 part

        // Send the Base64 encoded image data to the Flask server
        const response = await fetch('http://localhost:5000/analyze_emotion', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({ image: base64Image })
        });

        const data = await response.json();

        if (data.error) {
            console.error('Emotion analysis error:', data.error);
        } else {
            console.log('Detected Emotion:', data.emotion);
            document.getElementById('mood-display').textContent = `Mood: ${data.emotion}`;
        }
    } catch (error) {
        console.error('Error fetching emotion analysis:', error);
    }
}

// Start the camera and set up a periodic emotion analysis
document.addEventListener('DOMContentLoaded', () => {
    startCamera();
    setInterval(getEmotionAnalysis, 5000); // Adjust the interval as needed
});

